package cn.grin;

import java.util.Random;
import java.util.concurrent.Executors;

public class CommomServiceWindow {
	protected CustomerType type = CustomerType.COMMOM;
	protected int windowID = 1;
	protected NumberMachine nm = NumberMachine.getInstance();
	protected String windowName = null;
	public CommomServiceWindow(int windowID){
		this.windowID = windowID;
	}
	
	public void setType(CustomerType type) {
		this.type = type;
	}
	
	public void setWindowID(int windowID) {
		this.windowID = windowID;
	}
	
	
	public void start(){
		Executors.newSingleThreadExecutor().execute(new Runnable() {
			@Override
			public void run() {
				windowName = "第"+windowID+"号"+type+"窗口";
				while(true){
					work();
				}
			}
		});
	}

	protected void work() {
		Integer number = nm.getCommonManager().fetchServiceNumber();
		System.out.println(windowName+"获取"+CustomerType.COMMOM+"任务");
		if(null == number){
			System.out.println(windowName+"没有取到"+CustomerType.COMMOM+"任务，休息一秒钟");
			rest();
		}
		else{
			service(number,CustomerType.COMMOM,type == CustomerType.EXPRESS ? Constants.MIN_SERVICE_TIME : getSerivceTime());
		}
		
	}


	protected void rest() {
		try {
			Thread.sleep(1000);
		} catch (InterruptedException e) {
			// TODO 自动生成的 catch 块
			e.printStackTrace();
		}
	}

	protected void service(Integer number,CustomerType custType,long severTime) {
		System.out.println(windowName+"正在为第"+number+"号"+custType+"客户服务");
		
		try {
			Thread.sleep(severTime);
		} catch (InterruptedException e) {
			// TODO 自动生成的 catch 块
			e.printStackTrace();
		}
		
		System.out.println(windowName+"为第"+number+"个"+custType+"客户完成服务，耗时"+severTime/1000+"秒");
	}

	protected long getSerivceTime() {
		long maxRand = Constants.MAX_SERVICE_TIME - Constants.MIN_SERVICE_TIME;
		long severTime = new Random().nextInt((int)maxRand) + 1 + Constants.MIN_SERVICE_TIME;
		return severTime;
	}
	
	private void vipServer() {
		String windowName = "第"+windowID+"号"+type+"窗口";
		Integer number = nm.getVipManager().fetchServiceNumber();
		System.out.println(windowName+"获取VIP任务");
		if(null == number){
			System.out.println(windowName + "没有取到VIP任务");
			work();
			rest();
		}
		else{
			System.out.println(windowName+"正在为第"+number+"号vip客户服务");
			long beginTime = System.currentTimeMillis();
			long severTime = getSerivceTime();
			try {
				Thread.sleep(severTime);
			} catch (InterruptedException e) {
				// TODO 自动生成的 catch 块
				e.printStackTrace();
			}
			long costTime = System.currentTimeMillis() - beginTime;
			System.out.println(windowName+"为第"+number+"个"+"VIP"+"客户完成服务，耗时"+costTime/1000+"秒");
		}
	}
}
