
#include <iostream>

using std::cout;
using std::endl;


class A{
private:
	const int a;
	static const int b;		//静态常数据成员
public:
	A(int i);
	void Print();
};

const int A::b = 1;		//静态数据成员在类外说明和初始化

//常数据成员只能通过初始化列表来获得初值
A::A(int i): a(i) {}

void A::Print(){
	cout<<a<<':'<<b<<endl;
}

void main()
{
/***********************************************************
*	建立对象a1和a2，并以100和0作为初值，分别调用构造函数，通过
*	构造函数的初始化列表给对象的常数据成员赋初值
***********************************************************/
	A a1(100),a2(0);
	a1.Print();
	a2.Print();
}

